#include "SysTick.h"

static u8  fac_us=0;							//us๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ			   
static u16 fac_ms=0;							//ms๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
static u16 fac_s=0;								//s๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ


//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝำณูบ๏ฟฝ๏ฟฝ๏ฟฝ
//SYSTICK๏ฟฝ๏ฟฝสฑ๏ฟฝำนฬถ๏ฟฝฮชAHBสฑ๏ฟฝำต๏ฟฝ1/8
//SYSCLK:ฯตอณสฑ๏ฟฝ๏ฟฝฦต๏ฟฝ๏ฟฝ
void SysTick_Init(u8 SYSCLK)
{
	SysTick_CLKSourceConfig(SysTick_CLKSource_HCLK_Div8); 
	fac_us=SYSCLK/8;					
	fac_ms=(u16)fac_us*1000;			
	fac_s=fac_ms*1000;
}								    


/*******************************************************************************
* ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ         : delay_us
* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ		   : us๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : nus๏ฟฝ๏ฟฝาช๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝus๏ฟฝ๏ฟฝ
					ืข๏ฟฝ๏ฟฝ:nus๏ฟฝ๏ฟฝึต,๏ฟฝ๏ฟฝาช๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ798915us(๏ฟฝ๏ฟฝ๏ฟฝึต๏ฟฝ๏ฟฝ2^24/fac_us@fac_us=21)
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ๏ฟฝ๏ฟฝ
*******************************************************************************/		    								   
void delay_us(u32 nus)
{		
	u32 temp;	    	 
	SysTick->LOAD=nus*fac_us; 					//สฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ	  		 
	SysTick->VAL=0x00;        					//๏ฟฝ๏ฟฝีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	SysTick->CTRL|=SysTick_CTRL_ENABLE_Msk ;	//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ	  
	do
	{
		temp=SysTick->CTRL;
	}while((temp&0x01)&&!(temp&(1<<16)));		//๏ฟฝศด๏ฟฝสฑ๏ฟฝไตฝ๏ฟฝ๏ฟฝ   
	SysTick->CTRL&=~SysTick_CTRL_ENABLE_Msk;	//๏ฟฝุฑีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	SysTick->VAL =0X00;      					 //๏ฟฝ๏ฟฝีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ	 
}

/*******************************************************************************
* ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ         : delay_ms
* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ		   : ms๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : nms๏ฟฝ๏ฟฝาช๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝms๏ฟฝ๏ฟฝ
					ืข๏ฟฝ๏ฟฝ:nms๏ฟฝ๏ฟฝึต,SysTick->LOADฮช24ฮป๏ฟฝฤด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
					๏ฟฝ๏ฟฝาช๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ0xffffff*8*1000/SYSCLK
					๏ฟฝ๏ฟฝ72M๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ,nms<=1864ms 
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ๏ฟฝ๏ฟฝ
*******************************************************************************/
void delay_ms(u32 nms)
{	 		  	  
	u32 temp;		   
	SysTick->LOAD=(u32)nms*fac_ms;				//สฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(SysTick->LOADฮช24bit)
	SysTick->VAL =0x00;							//๏ฟฝ๏ฟฝีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	SysTick->CTRL|=SysTick_CTRL_ENABLE_Msk ;	//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ  
	do
	{
		temp=SysTick->CTRL;
	}while((temp&0x01)&&!(temp&(1<<16)));		//๏ฟฝศด๏ฟฝสฑ๏ฟฝไตฝ๏ฟฝ๏ฟฝ   
	SysTick->CTRL&=~SysTick_CTRL_ENABLE_Msk;	//๏ฟฝุฑีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	SysTick->VAL =0X00;       					//๏ฟฝ๏ฟฝีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ	  	    
} 


/*******************************************************************************
* ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ         : delay_s
* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ		   : s๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ns๏ฟฝ๏ฟฝาช๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝs๏ฟฝ๏ฟฝ
					ืข๏ฟฝ๏ฟฝ:nms๏ฟฝ๏ฟฝึต,SysTick->LOADฮช24ฮป๏ฟฝฤด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
					๏ฟฝ๏ฟฝาช๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ0xffffff*8*1000/SYSCLK
					
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ๏ฟฝ๏ฟฝ
*******************************************************************************/
void delay_s(u32 ns)
{	 		  	  
	u32 temp;		   
	SysTick->LOAD=(u32)ns*fac_s;				//สฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(SysTick->LOADฮช24bit)
	SysTick->VAL =0x00;							//๏ฟฝ๏ฟฝีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	SysTick->CTRL|=SysTick_CTRL_ENABLE_Msk ;	//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ  
	do
	{
		temp=SysTick->CTRL;
	}while((temp&0x01)&&!(temp&(1<<16)));		//๏ฟฝศด๏ฟฝสฑ๏ฟฝไตฝ๏ฟฝ๏ฟฝ   
	SysTick->CTRL&=~SysTick_CTRL_ENABLE_Msk;	//๏ฟฝุฑีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	SysTick->VAL =0X00;       					//๏ฟฝ๏ฟฝีผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ	  	    
} 


